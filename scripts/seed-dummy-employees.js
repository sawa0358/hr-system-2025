async function seedDummyEmployees() {
  try {
    console.log('🌱 ダミー社員データの投入を開始します...')
    
    // 既存の社員データをクリア
    await prisma.employee.deleteMany({})
    console.log('✅ 既存の社員データをクリアしました')
    
    // ダミー社員データを投入
    for (const employee of dummyEmployees) {
      await prisma.employee.create({
        data: employee
      })
    }
    
    console.log(`✅ ${dummyEmployees.length}件のダミー社員データを投入しました`)
    
    // 投入されたデータを確認
    const employees = await prisma.employee.findMany({
      select: {
        id: true,
        name: true,
        email: true,
        role: true,
        department: true,
        position: true,
        organization: true
      }
    })
    
    console.log('\n📊 投入された社員データ:')
    employees.forEach((emp, index) => {
      console.log(`${index + 1}. ${emp.name} (${emp.role}) - ${emp.email}`)
      console.log(`   部署: ${emp.department}, 役職: ${emp.position}`)
    })
    
  } catch (error) {
    console.error('❌ ダミー社員データの投入に失敗しました:', error)
  } finally {
    await prisma.$disconnect()
  }
}

seedDummyEmployees()
